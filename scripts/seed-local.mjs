/**
 * Seeds Firestore with all questions from LOCAL_QB in lib/categories.ts
 * Uses the Firebase CLIENT SDK — no service account needed.
 *
 * Run from the seen-jeem folder:
 *   node scripts/seed-local.mjs
 *
 * Requires: node 18+ and a valid .env.local file
 */

import { readFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

// ── Load .env.local manually ──────────────────────────────────────────────────
const envPath = resolve(__dirname, '../.env.local')
try {
  const envFile = readFileSync(envPath, 'utf8')
  for (const line of envFile.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eqIdx = trimmed.indexOf('=')
    if (eqIdx === -1) continue
    const key = trimmed.slice(0, eqIdx).trim()
    const val = trimmed.slice(eqIdx + 1).trim().replace(/^["']|["']$/g, '')
    process.env[key] = val
  }
  console.log('✓ Loaded .env.local')
} catch {
  console.error('✗ Could not read .env.local — make sure it exists in the project root')
  process.exit(1)
}

// ── Validate env vars ─────────────────────────────────────────────────────────
const required = [
  'NEXT_PUBLIC_FIREBASE_API_KEY',
  'NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN',
  'NEXT_PUBLIC_FIREBASE_PROJECT_ID',
  'NEXT_PUBLIC_FIREBASE_APP_ID',
]
const missing = required.filter(k => !process.env[k])
if (missing.length) {
  console.error('✗ Missing env vars:', missing.join(', '))
  process.exit(1)
}

// ── Firebase ──────────────────────────────────────────────────────────────────
const { initializeApp, getApps } = await import('firebase/app')
const { getFirestore, collection, writeBatch, doc, getDocs } = await import('firebase/firestore')

const app = getApps().length
  ? getApps()[0]
  : initializeApp({
      apiKey:     process.env.NEXT_PUBLIC_FIREBASE_API_KEY,
      authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN,
      projectId:  process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID,
      appId:      process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
    })

